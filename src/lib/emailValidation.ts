export type EmailValidationReason = 'format' | 'disposable' | 'suspicious';

export type EmailValidationResult =
  | { valid: true; normalised: string }
  | { valid: false; reason: EmailValidationReason };

function isSuspiciousLocalPart(local: string): boolean {
  // All identical characters (e.g. "aaaa", "1111")
  if (local.length > 1 && local.split('').every((c) => c === local[0])) return true;

  // Keyboard walks
  const l = local.toLowerCase();
  for (const walk of KEYBOARD_WALKS) {
    if (l.includes(walk)) return true;
  }

  return false;
}

/**
 * Validates an email address on format, disposable domain, and suspicious patterns.
 * Safe to call from both client and server — no Node.js APIs used here.
 */
export function validateEmail(raw: string): EmailValidationResult {
  const normalised = raw.trim().toLowerCase();

  if (!FORMAT_REGEX.test(normalised)) {
    return { valid: false, reason: 'format' };
  }

  const [local, domain] = normalised.split('@');

  if (DISPOSABLE_DOMAINS.has(domain)) {
    return { valid: false, reason: 'disposable' };
  }

  if (isSuspiciousLocalPart(local)) {
    return { valid: false, reason: 'suspicious' };
  }

  return { valid: true, normalised };
}

